INSERT INTO daily_words (word_id, game_date, language)
SELECT
    w.id,
    DATEADD('DAY', ROW_NUMBER() OVER (ORDER BY w.id) - 1, CURRENT_DATE),
    'pt-BR'
FROM words w
WHERE w.is_solution = TRUE
  AND w.language    = 'pt-BR'
  AND w.length      = 5
ORDER BY w.id
    LIMIT 30;